using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using E.sunbank0721.Data;
using E.sunbank0721.Models;

namespace E.sunbank0721.Pages.Products
{
    public class DetailsModel : PageModel
    {
        private readonly E.sunbank0721.Data.Esunbank0721Context _context;

        public DetailsModel(E.sunbank0721.Data.Esunbank0721Context context)
        {
            _context = context;
        }

      public Product Product { get; set; } = default!; 

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Product == null)
            {
                return NotFound();
            }

            var product = await _context.Product.FirstOrDefaultAsync(m => m.ID == id);
            if (product == null)
            {
                return NotFound();
            }
            else 
            {
                Product = product;
            }
            return Page();
        }
    }
}
